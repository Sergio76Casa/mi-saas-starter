export const formatCurrency = (amount: number, lang: Language) => {
  return new Intl.NumberFormat(lang === 'ca' ? 'ca-ES' : lang === 'fr' ? 'fr-FR' : lang === 'en' ? 'en-US' : 'es-ES', {
    style: 'currency',
    currency: 'EUR'
  }).format(amount);
};

export const formatDate = (dateString: string, lang: Language) => {
  if (!dateString) return '';
  return new Date(dateString).toLocaleDateString(lang === 'ca' ? 'ca-ES' : lang === 'fr' ? 'fr-FR' : lang === 'en' ? 'en-US' : 'es-ES', {
    year: 'numeric',
    month: 'long',
    day: 'numeric'
  });
};

